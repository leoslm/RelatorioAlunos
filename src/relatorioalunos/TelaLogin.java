    private void exibirInformacao(String info) {
        JOptionPane.showMessageDialog(this, info, "Atenção", JOptionPane.INFORMATION_MESSAGE);
    }

    private boolean validarCampos() {
        if (campoTextoLogin.getText().trim().length() == 0) {
            this.exibirInformacao("O valor do campo 'Login' não foi informado.");
            campoTextoLogin.requestFocus();
            return false;
        }
        if (campoTextoSenha.getText().trim().length() == 0) {
            this.exibirInformacao("O valor do campo 'Senha' não foi informado.");
            campoTextoSenha.requestFocus();
            return false;
        }
        return true;
    }

    public String doEfetuarLogin() {

        String novoLogin = campoTextoLogin.getText().replaceAll(" ", "");
        String novaSenha = campoTextoSenha.getText().replaceAll(" ", "");

        novoLogin = novoLogin.toLowerCase();
        novaSenha = novaSenha.toLowerCase();

        if ("login".equals(novoLogin)
                && "senha".equals(novaSenha)) {
            /* Se escrever o login e senha correto então vai para a tela principal do sistema. */
            this.setVisible(false);
            new TelaTexto().show();
            this.dispose();
        } else {
            this.exibirInformacao("Login ou senha incorretos");
        }

        //Caso erre o login ou senha fica na mesma tela.
        return null;
    }

