    private void campoTextoSenhaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_campoTextoSenhaKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            validarCampos();
            if (campoTextoSenha.getText().trim().length() != 0 && campoTextoLogin.getText().trim().length() != 0) {
                doEfetuarLogin();
            }
        }
        if (evt.getKeyCode() == KeyEvent.VK_ESCAPE) {
            if (javax.swing.JOptionPane.showConfirmDialog(null, "Deseja Fechar?", "ATENÇÂO ", javax.swing.JOptionPane.YES_NO_OPTION) == 0) {
                this.dispose();
            }
        }
        if (evt.getKeyCode() == KeyEvent.VK_SPACE) {
            this.exibirInformacao("A senha é sem espaços.");
            ExemploRobo();
        }

    }//GEN-LAST:event_campoTextoSenhaKeyPressed

